	 *
	 * @return void
	 */
	public function add_post_type() {

		$labels = array(
			'name'                  => _x( 'Services', 'Post Type General Name', 'design_ict_site' ),
			'singular_name'         => _x( 'Service', 'Post Type Singular Name', 'design_ict_site' ),
